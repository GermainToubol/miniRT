int	ft_isalnum(int c)
{
	int	is_alnum;

	is_alnum = (c >= 'a' && c <= 'z');
	is_alnum |= (c >= 'A' && c <= 'Z');
	is_alnum |= (c >= '0' && c <= '9');
	return (is_alnum);
}
