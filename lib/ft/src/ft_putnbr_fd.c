#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	buffer[10];
	int		i;

	if (n < 0)
		write(fd, "-", 1);
	i = 10 - 1;
	while (n != 0 || i == 10 - 1)
	{
		if (n >= 0)
			buffer[i] = '0' + n % 10;
		else
			buffer[i] = '0' - n % 10;
		n /= 10;
		i--;
	}
	write(fd, buffer + (i + 1), 10 - (i + 1));
}
